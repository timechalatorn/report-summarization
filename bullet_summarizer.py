import requests
from config import OLLAMA_API_URL, OLLAMA_MODEL_NAME

def summarize_chunk(text, index, temperature=0.7, seed=None):
    prompt = f"üìç ‡∏ï‡∏≠‡∏ô‡∏ó‡∏µ‡πà {index}\n\n‡∏™‡∏£‡∏∏‡∏õ‡∏ö‡∏ó‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ï‡πà‡∏≠‡πÑ‡∏õ‡∏ô‡∏µ‡πâ‡πÅ‡∏ö‡∏ö‡∏°‡∏µ‡∏£‡∏≤‡∏¢‡∏•‡∏∞‡πÄ‡∏≠‡∏µ‡∏¢‡∏î ‡πÅ‡∏ï‡πà‡∏Å‡∏£‡∏∞‡∏ä‡∏±‡∏ö:\n{text}\n\n‡∏ï‡∏≠‡∏ö‡πÄ‡∏õ‡πá‡∏ô‡∏¢‡πà‡∏≠‡∏´‡∏ô‡πâ‡∏≤‡πÄ‡∏î‡∏µ‡∏¢‡∏ß"
    payload = {
        "model": OLLAMA_MODEL_NAME,
        "prompt": prompt.strip(),
        "stream": False,
        "num_predict": 16384,
        "temperature": temperature
    }
    if seed is not None:
        payload["seed"] = seed
    response = requests.post(OLLAMA_API_URL, json=payload)
    if response.status_code == 200:
        return response.json().get("response", "[No summary returned]")
    return f"‚ùå Error: {response.status_code} - {response.text}"


